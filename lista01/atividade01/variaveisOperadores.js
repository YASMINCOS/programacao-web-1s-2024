var numA = 23;
var numB = 5;

var resultaSdooma = numA + numB;
console.log(`Resultado da adicao entre ${numA}  e  ${numB} : ${resultaSdooma}`);

var resultadoSubtracao = numA - numB;
console.log(`Resultado da subtração entre ${numA}  e  ${numB} : ${resultadoSubtracao}`);

var resultadoMultiplicacao = numA * numB;
console.log(`Resultado da multiplicacao entre ${numA}  e  ${numB} : ${resultadoMultiplicacao}`);

var resultadoDivisao = numA / numB;
console.log(`Resultado da divisão entre ${numA}  e  ${numB} : ${resultadoDivisao}`);

var resultadoResto = numA % numB;
console.log(`Resultado do resto entre ${numA}  e  ${numB} : ${resultadoResto}`);
